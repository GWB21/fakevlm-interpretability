print("\n[2] 카테고리별 핵심 지표")
print(f"{'카테고리':<12} {'총':>5} {'Vague%':>8} {'중복%':>7} {'TTR':>6} {'평균길이':>8}")
print("-"*48)
for c in CATE_ORDER:
    if c not in cate_stats:
        continue
    s = cate_stats[c]
    print(f"{c:<12} {s['n']:>5} {s['vague_ratio']:>7.1%} {s['dup_ratio']:>6.1%} "
          f"{s['ttr']:>6.3f} {s['avg_len']:>8.0f}")

print("\n[3] 상위 Vague Phrase 빈도 (상위 20개)")
for i, (p, v) in enumerate(list(phrase_freq.items())[:20], 1):
    origin = "seed" if p in SEED_VAGUE else "발굴"
    print(f"  {i:>2}. [{v:>4}회 / {v/N:.1%}] ({origin}) '{p}'")

print("\n[4] 가장 많이 반복된 첫 문장 (상위 10)")
for sent, cnt in top_first_sent:
    print(f"  [{cnt:>4}회] \"{sent[:80]}\"")

print("\n[5] 첫 5단어 도입부 패턴 (상위 15)")
for pat, cnt in top_first_5w:
    print(f"  [{cnt:>4}회] \"{pat}\"")

print("\n[6] 8-gram 반복 템플릿 (상위 15 / 등장률 5% 이상)")
for p, c in top8[:15]:
    print(f"  [{c:>4}회 / {c/N:.1%}] \"{p}\"")


