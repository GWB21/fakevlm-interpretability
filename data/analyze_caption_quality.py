# ── 1. 데이터 로드 ─────────────────────────────────────────────────
print("📂 데이터 로드 중...")
with open(JSON_PATH, encoding="utf-8") as f:
    raw = json.load(f)

records = []
for item in raw:
    for conv in item["conversations"]:
        if conv["from"] == "gpt":
            records.append({
                "cate":  item.get("cate", "unknown"),
                "label": item.get("label", -1),
                "text":  conv["value"].strip(),
                "lower": conv["value"].lower().strip(),
            })

N = len(records)
print(f"  총 {N:,}개 캡션 로드 완료\n")


# ── 2. N-gram Active 발굴 ─────────────────────────────────────────
def extract_ngrams(texts, n):
    all_ngrams = []
    for t in texts:
        words = re.findall(r"\b[a-z]+\b", t)
        all_ngrams += [" ".join(words[i:i+n]) for i in range(len(words) - n + 1)]
    return Counter(all_ngrams)

print("🔍 N-gram 분석 중 (4·6·8-gram)...")
all_lowers = [r["lower"] for r in records]
cnt4  = extract_ngrams(all_lowers, 4)
cnt6  = extract_ngrams(all_lowers, 6)
cnt8  = extract_ngrams(all_lowers, 8)

# 상위 N-gram 추출 (등장 빈도 > 전체의 5% = 250회 이상)
FREQ_THRESH = int(N * 0.05)
top4  = [(p, c) for p, c in cnt4.most_common(30) if c >= FREQ_THRESH]
top6  = [(p, c) for p, c in cnt6.most_common(20) if c >= FREQ_THRESH]
top8  = [(p, c) for p, c in cnt8.most_common(15) if c >= FREQ_THRESH]

# Active 발굴 후 VAGUE_PHRASES 확장
discovered_phrases = [p for p, _ in top6] + [p for p, _ in top8]
ALL_VAGUE = list(set(SEED_VAGUE + discovered_phrases))
print(f"  사전 정의: {len(SEED_VAGUE)}개 → Active 발굴 후 총 {len(ALL_VAGUE)}개 phrase\n")


# ── 3. Vague Coverage 계산 ────────────────────────────────────────
def vague_flags(record):
    t = record["lower"]
    hits = [p for p in ALL_VAGUE if p in t]
    return hits

for r in records:
    r["vague_hits"] = vague_flags(r)
    r["n_vague"]    = len(r["vague_hits"])
    r["is_vague"]   = r["n_vague"] > 0

n_vague = sum(r["is_vague"] for r in records)
print(f"📊 최소 1개 이상 vague phrase 포함 캡션: {n_vague:,} / {N:,} ({n_vague/N:.1%})")


# ── 4. 카테고리별 통계 집계 ───────────────────────────────────────
cate_stats = {}
for cate in CATE_ORDER:
    sub = [r for r in records if r["cate"] == cate]
    if not sub:
        continue
    fake_sub = [r for r in sub if r["label"] == 0]
    real_sub = [r for r in sub if r["label"] == 1]

    # TTR: Type-Token Ratio (어휘 다양성)
    all_words = re.findall(r"\b[a-z]+\b", " ".join(r["lower"] for r in sub))
    ttr = len(set(all_words)) / len(all_words) if all_words else 0

    # 평균 캡션 길이
    avg_len = np.mean([len(r["text"]) for r in sub])

    # Vague 비율
    v_ratio = np.mean([r["is_vague"] for r in sub])

    # 중복 캡션 비율 (완전 동일)
    texts = [r["lower"] for r in sub]
    dup_ratio = 1 - len(set(texts)) / len(texts)

    # Fake vs Real vague 비율 차이
    fake_v = np.mean([r["is_vague"] for r in fake_sub]) if fake_sub else 0
    real_v = np.mean([r["is_vague"] for r in real_sub]) if real_sub else 0

    cate_stats[cate] = {
        "n": len(sub), "n_fake": len(fake_sub), "n_real": len(real_sub),
        "ttr": ttr, "avg_len": avg_len,
        "vague_ratio": v_ratio,
        "dup_ratio": dup_ratio,
        "fake_vague": fake_v, "real_vague": real_v,
    }


# ── 5. 개별 phrase 빈도 계산 ─────────────────────────────────────
phrase_freq = {}
for p in ALL_VAGUE:
    cnt = sum(1 for r in records if p in r["lower"])
    phrase_freq[p] = cnt
phrase_freq = {k: v for k, v in sorted(phrase_freq.items(), key=lambda x: -x[1]) if v > 0}

# ── 6. 첫 문장 패턴 분석 ─────────────────────────────────────────
first_sentences = []
for r in records:
    sents = re.split(r'(?<=[.!?])\s+', r["text"])
    if sents:
        first_sentences.append(sents[0].lower().strip())

fs_counter = Counter(first_sentences)
top_first_sent = fs_counter.most_common(10)

# 첫 문장 도입부 패턴 (앞 5단어)
first_5w = []
for s in first_sentences:
    words = re.findall(r"\b[a-z]+\b", s)
    if len(words) >= 5:
        first_5w.append(" ".join(words[:5]))
top_first_5w = Counter(first_5w).most_common(15)


# ── 7. 리포트 출력 ────────────────────────────────────────────────
print("\n" + "="*65)
print("FakeClue Caption Quality Analysis — 리포트")
print("="*65)

